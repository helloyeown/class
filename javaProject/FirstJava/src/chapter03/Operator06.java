package chapter03;

public class Operator06 {

	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 2;
		float num3 = 7.0f;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 / 7);
		System.out.println(num1 / num3);
		System.out.println(num1 % num2);
		System.out.println(num1 % 7);
		
	}
}
