package chapter03;

public class Operator12 {

	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 2;
		
		System.out.println(true && true);
		System.out.println(true && false);
		System.out.println(false && false);

		System.out.println(num1 > 10 && num2 < 50);
		
		System.out.println();
		System.out.println(true || true);
		System.out.println(true || false);
		System.out.println(false || false);
		
		
		
		
	}
}
